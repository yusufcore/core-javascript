// 1) Arithmetic Operators
const sum = 5 + 8;  // Addition
const diff = 10 - 2;  // Subtraction
const p = 4 * 67;
const q = 8 / 2;
console.log(sum, diff, p, q);

// 2) Assignment Operators
let n =  10;
n += 5;
n *= 8; 
console.log(n);
// +=  --> adds and assigns the result to the variable.

// 3) Comparison Operators
console.log(10 > 5)
console.log(10 === "10");  // === ---> checks for both type and value.

// 4) Logical Operators
const a = true; b = false;
console.log(a && b);
console.log(a || b);
