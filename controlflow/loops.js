// For Loop

for (let i = 1; i <= 10; i++){
    if(i % 2 === 0){
        console.log(i);  
    }
}  // program to print even numbers from 1 to 10;

// While loop

let i = 1;

while(i <= 5){
    console.log(i);
    i++;
}

// Do-While loop

let j = 1;

do{
    console.log(j);
    j++;
}while (j <= 5);